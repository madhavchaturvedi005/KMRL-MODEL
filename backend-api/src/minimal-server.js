#!/usr/bin/env node

/**
 * Minimal server for testing deployment issues
 * This server has minimal dependencies and should start successfully
 */

import express from 'express';
import cors from 'cors';
import dotenv from 'dotenv';

// Load environment variables
dotenv.config();

const app = express();
const PORT = process.env.PORT || 3001;

// Basic middleware
app.use(cors());
app.use(express.json());

// Health check endpoint
app.get('/health', (req, res) => {
  res.status(200).json({
    status: 'healthy',
    timestamp: new Date().toISOString(),
    uptime: process.uptime(),
    environment: process.env.NODE_ENV || 'development',
    version: '1.0.0-minimal',
    port: PORT
  });
});

// Root endpoint
app.get('/', (req, res) => {
  res.json({
    name: 'KMRL Backend - Minimal Server',
    version: '1.0.0-minimal',
    status: 'running',
    message: 'This is a minimal server for testing deployment'
  });
});

// Test endpoint
app.get('/test', (req, res) => {
  res.json({
    message: 'Test endpoint working',
    timestamp: new Date().toISOString(),
    environment: process.env.NODE_ENV || 'development'
  });
});

// 404 handler
app.use('*', (req, res) => {
  res.status(404).json({
    error: 'Endpoint not found',
    path: req.originalUrl,
    method: req.method
  });
});

// Error handler
app.use((error, req, res, next) => {
  console.error('Error:', error);
  res.status(500).json({
    error: 'Internal server error',
    message: error.message
  });
});

// Handle uncaught exceptions
process.on('uncaughtException', (error) => {
  console.error('Uncaught Exception:', error);
  process.exit(1);
});

process.on('unhandledRejection', (reason, promise) => {
  console.error('Unhandled Rejection at:', promise, 'reason:', reason);
  process.exit(1);
});

// Start server
try {
  app.listen(PORT, '0.0.0.0', () => {
    console.log(`ðŸš€ Minimal KMRL Backend running on port ${PORT}`);
    console.log(`ðŸ“Š Environment: ${process.env.NODE_ENV || 'development'}`);
    console.log(`ðŸ”— Health check: http://localhost:${PORT}/health`);
    console.log(`âœ… Server started successfully`);
  });
} catch (error) {
  console.error('Failed to start minimal server:', error);
  process.exit(1);
}